package ru.polynkina.irina.beans;

public class MinMaxYear {

    private int minYear;
    private int maxYear;

    public MinMaxYear(int minYear, int maxYear) {
        this.minYear = minYear;
        this.maxYear = maxYear;
    }

    public int getMinYear() {
        return minYear;
    }

    public int getMaxYear() {
        return maxYear;
    }
}
